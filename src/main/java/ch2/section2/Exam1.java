public class Exam1 {
    public static void main(String[] args) {
        System.out.println("10 % 4 = " + (10 % 4));
        System.out.println("10 / 4 = " + (10 / 4));
        
        System.out.println("10 / 4 = " + ((double)10 / 4));
        System.out.println("10 / 4 = " + (double)(10 / 4));
    }
}
